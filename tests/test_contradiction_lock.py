class TestStateMachineFlow:
    """Test the full CLEAN → CONTRADICTION_DETECTED → RESOLVED → CLEAN flow."""

    def test_full_flow(self, tmp_path: Path) -> None:
        from vt_protocol.integrations.git_hooks import install_claude_code_hook

        root = tmp_path / "proj"
        root.mkdir()
        smm = root / ".smm"
        smm.mkdir()
        (smm / "traces").mkdir()
        (smm / "contradictions").mkdir()
        install_claude_code_hook(root)

        hook = root / ".claude" / "hooks" / "vt-validate.sh"
        lock = smm / "contradiction.lock"

        write_input = json.dumps({
            "tool_name": "Write",
            "tool_input": {"file_path": "/tmp/test.py", "content": "x = 1"},
        })

        # STATE: CLEAN — writes should pass
        r1 = subprocess.run(
            ["bash", str(hook)], input=write_input,
            capture_output=True, text=True, cwd=str(root), timeout=10,
        )
        assert "deny" not in r1.stdout

        # Simulate PostToolUse creating lock (CONTRADICTION_DETECTED)
        lock.write_text(json.dumps({
            "contradiction_id": "flow-test",
            "detected_at": "2026-04-14T09:00:00Z",
            "description": "A vs B",
            "message": "Resolve via dashboard",
        }))

        # STATE: CONTRADICTION_DETECTED — writes should be blocked
        r2 = subprocess.run(
            ["bash", str(hook)], input=write_input,
            capture_output=True, text=True, cwd=str(root), timeout=10,
        )
        output2 = json.loads(r2.stdout)
        assert output2["hookSpecificOutput"]["permissionDecision"] == "deny"

        # STATE: CONTRADICTION_DETECTED — self-resolve should be blocked
        r3 = subprocess.run(
            ["bash", str(hook)],
            input=json.dumps({
                "tool_name": "Bash",
                "tool_input": {"command": "vt check --resolve"},
            }),
            capture_output=True, text=True, cwd=str(root), timeout=10,
        )
        output3 = json.loads(r3.stdout)
        assert output3["hookSpecificOutput"]["permissionDecision"] == "deny"

        # Human resolves — delete lock (RESOLVED → CLEAN)
        lock.unlink()

        # STATE: CLEAN — writes should pass again
        r4 = subprocess.run(
            ["bash", str(hook)], input=write_input,
            capture_output=True, text=True, cwd=str(root), timeout=10,
        )
        assert "deny" not in r4.stdout
