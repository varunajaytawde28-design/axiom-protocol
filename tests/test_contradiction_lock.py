class TestPreToolUseHookExecution:
    """Run the actual vt-validate.sh hook script and verify behavior."""

    def _run_hook(self, tmp_path: Path, input_json: dict) -> subprocess.CompletedProcess:
        from vt_protocol.integrations.git_hooks import install_claude_code_hook

        root = tmp_path / "proj"
        root.mkdir()
        (root / ".smm").mkdir()
        (root / ".smm" / "traces").mkdir(parents=True, exist_ok=True)
        install_claude_code_hook(root)

        hook = root / ".claude" / "hooks" / "vt-validate.sh"
        return subprocess.run(
            ["bash", str(hook)],
            input=json.dumps(input_json),
            capture_output=True,
            text=True,
            cwd=str(root),
            timeout=10,
        )

    def test_allows_write_when_no_lock(self, tmp_path: Path) -> None:
        result = self._run_hook(tmp_path, {
            "tool_name": "Write",
            "tool_input": {"file_path": "/tmp/test.py", "content": "x = 1"},
        })
        # Should not deny (vt command likely not installed in test env)
        if result.stdout.strip():
            output = json.loads(result.stdout)
            # If there's output, it shouldn't be a lock-based denial
            reason = (
                output.get("hookSpecificOutput", {})
                .get("permissionDecisionReason", "")
            )
            assert "Unresolved contradiction" not in reason

    def test_blocks_write_when_lock_exists(self, tmp_path: Path) -> None:
        from vt_protocol.integrations.git_hooks import install_claude_code_hook

        root = tmp_path / "proj"
        root.mkdir()
        smm = root / ".smm"
        smm.mkdir()
        (smm / "traces").mkdir()
        install_claude_code_hook(root)

        # Create lock file
        lock = smm / "contradiction.lock"
        lock.write_text(json.dumps({
            "contradiction_id": "test-123",
            "detected_at": "2026-04-14T09:00:00Z",
            "description": "NoSQL vs Relational",
            "message": "Resolve via dashboard",
        }))

        hook = root / ".claude" / "hooks" / "vt-validate.sh"
        result = subprocess.run(
            ["bash", str(hook)],
            input=json.dumps({
                "tool_name": "Write",
                "tool_input": {"file_path": "/tmp/test.py", "content": "x = 1"},
            }),
            capture_output=True,
            text=True,
            cwd=str(root),
            timeout=10,
        )
        assert result.returncode == 0
        output = json.loads(result.stdout)
        decision = output["hookSpecificOutput"]["permissionDecision"]
        reason = output["hookSpecificOutput"]["permissionDecisionReason"]
        assert decision == "deny"
        assert "Unresolved contradiction" in reason

