class TestGenerateClaudeRules:
    def test_generates_rule_files(self, decisions: list[Decision], tmp_path: Path) -> None:
        scored = assign_tiers(decisions, always_count=1, auto_count=10)
        auto = [s for s in scored if s.tier == "auto"]
        if auto:
            paths = generate_claude_rules(scored, tmp_path / "rules")
            assert len(paths) > 0
            for p in paths:
                assert p.suffix == ".md"
                content = p.read_text()
                assert "---" in content  # YAML frontmatter
                assert "globs:" in content
                assert "alwaysApply: false" in content

    def test_creates_output_dir(self, decisions: list[Decision], tmp_path: Path) -> None:
        scored = assign_tiers(decisions, always_count=1, auto_count=10)
        rules_dir = tmp_path / "new" / "rules"
        generate_claude_rules(scored, rules_dir)
        assert rules_dir.is_dir()


