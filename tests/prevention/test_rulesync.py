class TestSyncRules:
    def test_full_sync(self, decisions: list[Decision], config: GovernanceConfig, tmp_path: Path) -> None:
        # Create .smm/generated/ dir
        (tmp_path / ".smm" / "generated").mkdir(parents=True)
        result = sync_rules(decisions, tmp_path, config)
        assert len(result.files_written) > 0
        assert result.always_count + result.auto_count + result.on_demand_count == len(decisions)

    def test_creates_agents_md_at_root(self, decisions: list[Decision], config: GovernanceConfig, tmp_path: Path) -> None:
        (tmp_path / ".smm" / "generated").mkdir(parents=True)
        sync_rules(decisions, tmp_path, config)
        assert (tmp_path / "AGENTS.md").is_file()
        content = (tmp_path / "AGENTS.md").read_text()
        assert "AGENTS.md" in content

    def test_creates_claude_files_when_enabled(self, decisions: list[Decision], tmp_path: Path) -> None:
        config = GovernanceConfig(agents={"claude": True, "cursor": False})
        (tmp_path / ".smm" / "generated").mkdir(parents=True)
        result = sync_rules(decisions, tmp_path, config)
        generated_claude = [f for f in result.files_written if "CLAUDE.md" in f.name]
        assert len(generated_claude) > 0

    def test_skips_cursor_when_disabled(self, decisions: list[Decision], tmp_path: Path) -> None:
        config = GovernanceConfig(agents={"claude": False, "cursor": False})
        (tmp_path / ".smm" / "generated").mkdir(parents=True)
        result = sync_rules(decisions, tmp_path, config)
        cursor_files = [f for f in result.files_written if ".cursor" in str(f)]
        assert len(cursor_files) == 0
