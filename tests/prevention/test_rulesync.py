def _make_decision(title: str, dimensions: list[Dimension] | None = None) -> Decision:
    return Decision(
        title=title,
        content=f"Decision about {title}. This is the full description.",
        rationale=f"Because {title} is the best choice.",
        decision_type=DecisionType.ARCHITECTURAL,
        dimensions=dimensions or [Dimension.DATABASE],
        made_by="test",
        project="test-project",
        source_type=SourceType.MANUAL,
    )


